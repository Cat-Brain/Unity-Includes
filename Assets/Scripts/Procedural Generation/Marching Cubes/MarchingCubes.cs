using UnityEngine;

[System.Serializable]
public class MCChunk
{
    public MeshFilter mf;
    public MeshCollider collider;

    [HideInInspector] public float[,,] data;
    [HideInInspector] public float threshold;

    [HideInInspector] public Mesh mesh;
}

public static class MarchingCubes
{
    // Used by all UpdateChunk for all chunks. Values are not reused between chunks but the memory is
    private static int[,,] tableIndices;

    // Uses data to construct a mesh and if there is a MeshFilter or MeshCollider it sets their respective meshes to that mesh
    public static void UpdateChunk(ref MCChunk chunk)
    {
        if (chunk == null || chunk.data == null) return;

        if (tableIndices == null || tableIndices.GetLength(0) != chunk.data.GetLength(0) - 1)
            tableIndices = new int[chunk.data.GetLength(0) - 1, chunk.data.GetLength(1) - 1, chunk.data.GetLength(2) - 1];

        chunk.mesh = new Mesh();

        int vertCount = 0;

        for (int x = 0; x < chunk.data.GetLength(0) - 1; x++)
            for (int y = 0; y < chunk.data.GetLength(1) - 1; y++)
                for (int z = 0; z < chunk.data.GetLength(2) - 1; z++)
                    vertCount += marchingCubesTable[tableIndices[x, y, z] = GetCase(chunk, x, y, z), 0];

        Vector3[] verts = new Vector3[vertCount];
        int[] tris = new int[vertCount];

        int index = 0; // Used by both vertices and triangles

        Vector3Int pos = Vector3Int.zero;
        for (pos.x = 0; pos.x < chunk.data.GetLength(0) - 1; pos.x++)
            for (pos.y = 0; pos.y < chunk.data.GetLength(1) - 1; pos.y++)
                for (pos.z = 0; pos.z < chunk.data.GetLength(2) - 1; pos.z++)
                {
                    int tableIndex = tableIndices[pos.x, pos.y, pos.z];
                    for (int i = 0; i < marchingCubesTable[tableIndex, 0]; i++)
                    {
                        int triIndex = marchingCubesTable[tableIndex, i + 1];
                        (byte first, byte second) edgeIndices = marchingCubesEdgeTable[triIndex];
                        Vector3Int firstPos = pos + marchingCubesCornerTable[edgeIndices.first],
                            secondPos = pos + marchingCubesCornerTable[edgeIndices.second];
                        float firstValue = chunk.data[firstPos.x, firstPos.y, firstPos.z],
                            secondValue = chunk.data[secondPos.x, secondPos.y, secondPos.z];
                        verts[index] = Vector3.Lerp(firstPos, secondPos,
                            (chunk.threshold - firstValue) / (secondValue - firstValue));

                        tris[index] = index;
                        index++;
                    }
                }

        chunk.mesh.SetVertices(verts);
        chunk.mesh.SetTriangles(tris, 0);
        chunk.mesh.RecalculateNormals();
        if (chunk.mf)
            chunk.mf.mesh = chunk.mesh;
        if (chunk.collider)
            chunk.collider.sharedMesh = chunk.mesh;
    }

    // Returns which index a given coordinate (local to chunk) should use for mesh construction
    public static byte GetCase(in MCChunk chunk, int x, int y, int z)
    {
        byte result = 0;

        if (chunk.data[x, y, z] > chunk.threshold)
            result |= 1;
        if (chunk.data[x + 1, y, z] > chunk.threshold)
            result |= 2;
        if (chunk.data[x, y + 1, z] > chunk.threshold)
            result |= 4;
        if (chunk.data[x + 1, y + 1, z] > chunk.threshold)
            result |= 8;
        if (chunk.data[x, y, z + 1] > chunk.threshold)
            result |= 16;
        if (chunk.data[x + 1, y, z + 1] > chunk.threshold)
            result |= 32;
        if (chunk.data[x, y + 1, z + 1] > chunk.threshold)
            result |= 64;
        if (chunk.data[x + 1, y + 1, z + 1] > chunk.threshold)
            result |= 128;

        return result;
    }

